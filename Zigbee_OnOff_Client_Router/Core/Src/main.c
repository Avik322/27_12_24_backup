double calculate_polynomial(double RAW_VWC, double* coeffs, int coeffs_size) {
    double result = 0.0;
    int degree = coeffs_size - 1;

    for (int i = 0; i < coeffs_size; ++i) {
        result += coeffs[i] * pow(RAW_VWC, degree - i);
    }
    return result;
}
